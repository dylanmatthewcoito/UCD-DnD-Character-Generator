const mongoose = require('mongoose');

const statsSchema = new mongoose.Schema({
    strength: {
        type: Number,
        required: true
    },
    dexterity: {
        type: Number,
        required: true
    },
    constitution: {
        type: Number,
        required: true
    },
    intelegence: {
        type: Number,
        required: true
    },
    wisdom: {
        type: Number,
        required: true
    },
    charisma: {
        type: Number,
        required: true
    }
})

const Stats = mongoose.model('Stats', statsSchema);

module.exports = Stats;